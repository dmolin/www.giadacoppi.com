    <p><em>Part 1.</em> Starting from the photocopies provided, analyze letterforms of the original face
    designed by Pietro Bembo in 1495, and trace them on a clean tracing paper sheet. Pay attention to the details
    and fill in the ruined/missing parts with the support of the complete digital version of the same face.</p>
    <p><em>Part 2.</em> Starting from your reconstruction of Bembo letterforms, design a new typeface that
    is inspired by it: elaborate or subtly modify one detail of the alphabet and apply it consistently
    to all the 26 letters.</p>

    <p><b>Project goals:</b> get familiar with the forms and the proportions of letter shapes, understanding and
    appreciating the individual details, as well as the patterns and common traits across the letters.</p>
    <p><b>Final delivery:</b> draw the entire lowercase alphabet of your Bembo rendition.
    The final piece will be black and white, on a clean sheet 11x17 inches, mounted on a 15x20 inches black board.</p>
    <p><b>Project duration:</b> two weeks.</p>
    <p><b>Reading assignment:</b> Making letters work, from <em>Lettering & Type</em> by B.Willen and N.Strals (pag.99-123).

    <p class="source">Inspirational source: exercise proposed by Lucio Passerini and James Clough during the course
    &#8220;Typographic design&#8221; (Design tipografico) held at <a href="http://www.cfpbauer.it/">CFP Riccardo Bauer</a>.</p>


