    <p>For every class meeting you will collect at least one sample of letter forms that you like.
    They can be cut out from magazines or packagings, photocopies of books, photographs of signs, sketches
    made by you. Each piece will be accompanied by some critical annotation. By the end of the semester,
    you will have at least 32 pieces. </p>
    <p><a href="http://typographyjournal.blogspot.com" target="_blank">An example of digital type journal</a>.</p>

    <p><b>Project goals:</b> develop consciousness of the various forms of typography, learn how to look
    for original and interesting letterforms, get acquainted with vernacular typography.</p>
    <p><b>Final delivery:</b> a plain notebook, one entry per page accompanied by its comment.</p>
    <p><b>Project duration: </b>over the course of the semester.</p>


