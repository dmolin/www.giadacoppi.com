    <p>Typography can be described as having two main uses: functional (putting it down simply, when its final use is reading)
    and aesthetical (when letters are treated as decorative elements). In this exercise you will explore the aesthetical
    power of letterforms by creating textures and patterns made out of single letters</p>
    <p>Sketch and play around with a few letters you are particularly interested on (either uppercase or lowercase). Pick five
    letters and use one (or two) at the time as a modular element for creating a pattern: letters can be translated, rotated, over-layed
    as many times as you please.</p>
    <p>Draw the final letters on tracing paper, and pass them on the rubber block. Cut out the letter from the rubber block (remember that the
    image had to be mirrored), ink it and experiment on paper. Stamping will not only give you the opportunity to repeat the same identical letter
    without the need of drawing it over and over, but will also allow space for sperimentation.</p>

    <p><b>Project goals:</b> increase your sensitivity to letterforms structure, and experience the manual
    process,and how this can lead to original and unexpected solutions..</p>
    <p><b>Final delivery:</b> five textures, each hand-stamped on a white sheets of paper 10x10 inches.
    <p><b>Project duration:</b> one day workshop.</p>

    <p class="source">Inspirational source: exercise &#8220;La composizione ornamentale&#8221; from the book
    <em>Delle lettere</em> by I. Tubaro.</p>


