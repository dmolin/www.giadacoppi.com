<div id="project-col">
    <p>Create an alphabet by designing letters on a squared grid.
    Substitute the curves and diagonals of traditional letterforms with gridded
    and rectilinear elements. Avoid making detailed "staircases," which are just
    curves and diagonals in disguise.</p>

    <p><b>Project goals:</b> study the simple geometric parts of letterforms and their
    repetition across the alphabet, and identify the patterns that make these shapes
    work together as a system.</p>
    <p><b>Final delivery:</b> Using a black marker, draw the 26 letters of the alphabet in uppercase and lowercase
    on a clean layout sheet 11x17 inches. Print your name on the lower right hand corner.</p>
    <p><b>Project duration:</b> one week.</p>


