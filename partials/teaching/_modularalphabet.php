<div id="project-col">
    <p>Create an alphabet using only a small set (between 3-6 pieces) of basic geometric shapes.
    Begin by drawing the letters of the alphabet in lowercase and uppercase and examine them
    carefully. Work on a grid that can fit the modular elements you plan to use.</p>
    <p>After defining the reference grid, design the letters that include all the basic forms
    (i.e. round, square and diagonal letters). Refine the system as needed, with the aim of making
    the letterforms look consistent. Construct the other characters based on these first letters.
    Test the letters in word combinations periodically along the development of the entire alphabet
    to spot inconsistencies and check their &#8220;visual rhythm&#8221;.
    You can design either lowercase or uppercase letters. If your grid system (and the timeline)
    supports you, you can of course design both.</p>

    <p><b>Project goals:</b> get familiar with the forms and the proportions of letter shapes, understanding and
    appreciating the individual details, as well as the patterns and common traits across the letters.</p>

    <p><b>Final delivery:</b> One sheet of layout paper 12x17 inches, displaying the entire alphabet and presenting the grid
    system and how the construction of letters works. Use only black marker. The project is to be mounted on 15x20 inches black board.</p>

    <p><b>Project duration:</b> three weeks.</p>

    <p class="source">Inspirational source: <em>Lettering & Type</em> by B. Willen and N. Strals.</p>

