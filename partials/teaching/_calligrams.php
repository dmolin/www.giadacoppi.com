    <p>A calligram is a poetic composition that is meant to be appreciated by both reading and looking at
    it (visual poem). Letter-forms are used as visual elements to create an image that expresses what
    the words say. For instance, if a text is talking about a castle, letters and words are displayed in
    such a way to form the image of a castle. Other times, words and image are completely disconnected.</p>

    <p>The word &#8220;calligram&#8221; stems from Greek <em>kalligramma</em>, that is <em>kalos</em> (=beautiful) + <em>gramma</em>
    (=letter), and it is in fact in the Greek culture (around VI-III century BC) that the first forms
    of calligrams are created. The phenomenon really flourished, though, at the beginning of the XX
    century, during the avant-garde artistic movements, in particular with the work of Guillaume Apollinaire.</p>

    <p>For this project, you will create six calligrams, submitting one every two weeks. Texts content can be made up
    or taken from a poem/song/book. The set of calligrams will refer to a common theme of your choice.</p>

    <p><b>Project goals:</b> explore the fuzzy border between letter and image.</p>
    <p><b>Final delivery:</b> six calligrams, created by the medium you find more suitable to your needs.
    Format is free (something I can take home for grading!). All calligrams have to be the same size.</p>
    <p><b>Project duration:</b> over the course of the semester.</p>

