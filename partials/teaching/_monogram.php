<div id="project-col">
    <p>A monogram is a motif made by overlapping or combining two or more letters or other graphemes
    to form one symbol. Create a monogram of your name or a pseudonym you like to use as a signature: you can start
    from a typeface and elaborate it, do it calligraphic or illustrated. Black color only.</p>

    <p><b>Project goals:</b> play with couples of letters not only by putting them aside, but in search of original ways
    to overlap and ligate them.</p>
    <p><b>Final delivery:</b> 10x10 inches sheet, presenting the monogram in black and white (the scale of the monogram should
    be reasonably proportioned to the sheet size)</p>
    <p><b>Project duration: </b>two weeks.</p>


    <p class="source">Inspirational source: exercise &#8220;Il monogramma&#8221; from the book
    <em>Delle lettere</em> by I. Tubaro.</p>

