    <p>The objective of this project is to communicate the meaning of a word by exploiting typography.</br>
    Choose two words (adjectives and verbs may be easier) and write down their meanings and
    some of their synonyms taken from the dictionary. In a typeface (or typefaces) picked
    from the selection provided&#42;, render each word in a way that visually amplifies
    its verbal meaning.
    You may use scale, repetition, letter spacing, overlapping and any other typographic
    and layout devices to achieve a successful rendering of your words.
    The results should communicate some surprise or awareness not perceived when the word
    is presented in standard typographic form.</p>
    <p>Once you have picked the words and made some sketches on how to render them, pick one or more of the specimens
    and make a bunch of copies. Cut out the letters you need and use them to create your composition on a clean sheet of paper.
    Glue them together, minding about baselines, letterspacing, alignment etc. The final pieces are to be traced from this composition.</p>

    <p>&#42;The selection provided included printed specimens of the following typefaces in various weights: Garamond, Baskerville, Adobe Caslon, Century Schoolbook,
    Rockwell, Bodoni, Didot, Gill Sans, Helvetica, Gotham, Neutra Text, Optima, Letter Gothic.</p>

    <p><b>Project goals:</b> familiarize with composition and letterspacing, practice in drawing letters and explore
    the potential of typography as a visual tool.</p>
    <p><b>Final delivery:</b> Draw each word on a sheet of layout paper sized to 11x11 inches,
    and mounted on 15x20 inches black board. Use all or part of the page for your composition
    as appropriate. Black and white only.</p>
    <p><b>Project duration:</b> three weeks.</p>


