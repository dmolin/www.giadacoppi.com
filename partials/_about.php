<div id="middle-col">
    <div class="title">Profile</div>
    <p>I am an Italian graphic designer and letterer, who happened to relocate a few times in the recent years.
    In 2009 I moved from Milan (Italy), the city where I grew up, to Nashville Tennessee (USA), where I lived and worked
    for three years. As of September 2012, I live in Vienna, Austria.</br>
    I hold BA and MA Degrees in Communication and Graphic Design from the Politecnico di Milano,
    and a MA in Information Design from the University of Reading (UK). This last English experience, together
    with a course attended at CFP Riccardo Bauer in Milan, gave me the opportunity to really get acquainted
    and fall in love once and for all with with typography and letterpress printing.
    </p>
    </br>
    <div class="title">Teaching</div>
    <p>Teaching has been part of my professional experience since the very beginning: I started as a teaching assistant
    at the Politecnico di Milano, I have then been lecturer at IED (European Design Institute) Milano for two years,
    leading a Graphic Design course, in collaboration with art director Stefano Temporin. In Nashville I have worked
    as adjunct faculty at Watkins College of Art, Design & Film, where I taught classes on Graphic Design and Typography.
    I have also participated to various workshops, in collaboration with CFP Riccardo Bauer and Libreria 121.
    </p>
</div>

<div id="right-col">
    <div class="title">Design work</div>
        <p>Collaborations and team work have always been an significant aspect of my professional growth. While still in school, I ventured
        with a small group of fellow classmates into starting something that later became LS graphic design, officially founded in 2006
        by Marta Bernstein, Alberto Cantone, Paolo Ciampagna, Emanuela Conidi and myself. During these years of prolific collaboration,
        I have gained experience in graphic design, as well as in the practice of running a studio. When I moved to Nashville, while keeping
        up the collaboration with LS, I have had the opportunity to work with some great local creatives, such as art director Jeff Perky
        (Perky Bros LLC), illustrator Dan Brawner and artist and letterpress printer Bryce McCloud (Isle of Printing).
        </p>
    </br>
    <div class="title">Skills and interests</div>
        <p>Printed and editorial matter, visual identity and packaging are among the services that can be provided. I particularly
        enjoy creating hand drawn lettering, using and mixing diverse media and experiment with handcrafted printing techniques,
        looking for distinctive solutions.
        </p>
</div>