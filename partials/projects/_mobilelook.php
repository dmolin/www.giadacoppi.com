    <h2 class="project-title">The Mobile City</h2>
    <h3 class="project-subtitle">Book</h3>

        <p>A small book for smaller photographs. The Mobile City book collects the results of a photographic contest
        organized by <a href="http://www.mufoco.org/" target="_blank">Museo di Fotografia Contemporanea</a> (Cinisello Balsamo, Milan),
        that required participants to submit non-professional images related to city life in Milan and Toronto.
        Photos had to be taken only with a mobile phone. The book format and layout accompany this material, and aim to recount
        the urban style of the photographs.</p>

        <div class="project-info"><p>Designed at LS graphic design.</br>
        2008.</p>
