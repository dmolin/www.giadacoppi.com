    <h2 class="project-title">Manyval</h2>
    <h3 class="project-subtitle">Lettering and poster</h3>

    <p>ManyVal (short for Many Valued Logic) is an annual conference organized
    by the Department of Information and Communication Technology
    of The University of Milan.</p>

    <p>The lettering <em>ManyVal</em> was constructed using <strong>Tribasei</strong>,
    a modular font designed by <a href="http://www.molotro.com/" target="_blank">Molotro</a>
    based on the historic Fregio Mechano.
    The font includes a system of geometric figures having with a modular pattern
    that can be combined in an infinite number of ways to create letters
    and decorations.</p>

