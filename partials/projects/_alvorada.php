    <h2 class="project-title">Alvorada</h2>
    <h3 class="project-subtitle">Lettering for a sail ship</h3>

    <p>Alvorada, &#8220;sunrise&#8221; in Portuguese, is a sail ship that moors in the harbor of Pescara (Italy).
    Originally created with a flat-nib pen, the lettering is intentionally quite naive, and has the terminals
    of the first and the last letters exaggeratedly long, as if dragged by the wind. The lettering appears
    at the stern of the boat, on the sails cover and on the crew members shirts.</p>

