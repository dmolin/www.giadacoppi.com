    <h2 class="project-title">Graffiti</h2>
    <h3 class="project-subtitle">Custom lettering</h3>


        <p><a href="http://www.graffiti2000.com/" target="_blank">Graffiti</a> is a web and graphic design agency located in Arco di Trento, Italy.
        They asked for a custom lettering to be associated with their new identity. The lettering
        is applied to six sentences, describing their core services: <em>it&#8217;s communication</em>, the main payoff,
        <em>mobile solutions</em>, <em>web & social marketing</em>, <em>media planning</em>, <em>communication strategy</em>
        and <em>software</em>.</p>

        <p>The lettering was first created on paper, and then made vectorial to make it
        usable for any type of application, printed and digital.</p>

