    <h2 class="project-title">Liga Masiva</h2>
    <h3 class="project-subtitle">Coffee bag</h3>

    <p>Hand-grown by a small group of farmers in the Dominican Republic, Liga Masiva coffee is an organic, direct trade, quality product.
    The bag design aims to recount all this through a genuine hand drawn lettering style.</p>

    <p>Some of the product information, such as the date and type of roasting, was designed to be hand-stamped on the bag
    at the very last minute: this responded to the issue of a small run screen-printing, as well as contributed to
    emphasize the product&#8217;s freshness.</p>

